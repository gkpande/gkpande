class Solution{
  public:
  
    //Function to find starting point where the truck can start to get through
    //the complete circle without exhausting its petrol in between.
    int tour(petrolPump p[],int n)
    {
       int rp=0,prp=0; //rp=remaining petrol
       int f=0;
       
       for(int i=0;i<n;i++){
           rp=rp+p[i].petrol-p[i].distance;
           if(rp<0){
               prp=prp+rp;
               rp=0;
               f=i+1;
           }
        }
        if(prp+rp>=0){
            return f;
        }
       return -1;
    }
};



